"""
字符串中的转义字符：
    （1）\t 在控制台输出一个制表符，协助在输出文本时垂直方向保持对齐；
    （2）\n 在控制台输出一个换行符；

制表符的功能是在不使用表格的情况下在垂直方向按列对齐文本
"""
# （1）\t 在控制台输出一个制表符，协助在输出文本时垂直方向保持对齐；
print("1 2 3")
print("10 20 30")

print("-" * 10)

print("1\t2\t3")
print("10\t20\t30")

# （2）\n 在控制台输出一个换行符；
print("=" * 10)

print("Hello Python")

print("-" * 10)

print("Hello\n Python")

# （2）\" 在控制台输出一个换行符；
print("=" * 10)

print("Hello\"Python")